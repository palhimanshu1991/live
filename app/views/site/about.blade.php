                    We all talk about movies in our day to day lives with our friends, colleagues, relatives (ok, not with relatives) and sometimes even with strangers.
                    <br><br>
                    We discuss movies we watched, we ask for movie recommendations from our friends, we tell our friends about the awesome movie we recently saw and ask them they to see it too. But not much of these activities happen online.
                    <br><br>

                    Before starting Berdict, we did a small experiment and invited a group of friends and collected their reviews on some famous and recent movies and also the critic reviews for the same movies.

                    Then, we displayed both the user reviews and the critic reviews on the respective movies, to figure out whose opinion matters more to people when it comes to movies.
                    <br><br>
                    We found that, the opinion of friends matter more as compared to the critics because the users were reading almost all the reviews of their friends.
                    <br><br>
                    The critic reviews were long, so users just looked at the rating which was given by the critic and read one or two paragraphs of the whole review.	
                    <br><br>
                    That's when we thought of starting Berdict to solve all these problems.
                    <br><br>
                    And In case if you're wondering about the name, "Berdict" is a pun of Brief and Verdict, which is the essence of the reviews on berdict i.e. to be short and to the point.
